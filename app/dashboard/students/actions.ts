'use server'

import { createClient } from '@/lib/supabase/server'
import { createAdminClient } from '@/lib/supabase/admin'
import { revalidatePath } from 'next/cache'
import type { TrainingStatus } from '@/types'

export interface AddStudentInput {
  full_name: string
  phone: string
  email: string        // required — used for login
  password?: string    // optional — auto-generated if empty
  license_category: string
  driving_school_id?: string // required for super_admin only
}

function generatePassword(): string {
  // Omit ambiguous chars (0, O, l, 1, I) for readability
  const chars = 'ABCDEFGHJKMNPQRSTUVWXYZabcdefghjkmnpqrstuvwxyz23456789'
  return Array.from({ length: 10 }, () =>
    chars[Math.floor(Math.random() * chars.length)]
  ).join('')
}

export async function addStudent(
  input: AddStudentInput
): Promise<{ error?: string; createdPassword?: string }> {
  // ── 1. Verify caller is an admin ──────────────────────────
  const supabase = await createClient()
  const { data: { user } } = await supabase.auth.getUser()
  if (!user) return { error: 'Non authentifié.' }

  const { data: adminProfile } = await supabase
    .from('profiles')
    .select('role, driving_school_id')
    .eq('user_id', user.id)
    .single()

  if (!adminProfile) return { error: 'Profil introuvable.' }

  let schoolId: string

  if (adminProfile.role === 'school_admin') {
    if (!adminProfile.driving_school_id) {
      return { error: 'Aucune auto-école liée à ce compte.' }
    }
    schoolId = adminProfile.driving_school_id
  } else if (adminProfile.role === 'super_admin') {
    if (!input.driving_school_id) {
      return { error: 'Veuillez sélectionner une auto-école.' }
    }
    schoolId = input.driving_school_id
  } else {
    return { error: 'Accès refusé.' }
  }

  if (!input.email?.trim()) {
    return { error: "L'email est obligatoire pour créer un compte élève." }
  }

  // ── 2. Determine password ─────────────────────────────────
  const isAutoGenerated = !input.password?.trim()
  const password = isAutoGenerated ? generatePassword() : input.password!.trim()

  // ── 3. Create auth user (admin API — bypasses email confirmation) ─
  const admin = createAdminClient()

  const { data: authData, error: authError } = await admin.auth.admin.createUser({
    email: input.email.trim(),
    password,
    email_confirm: true,
  })

  if (authError) {
    const msg = authError.message.includes('already registered')
      ? 'Cette adresse email est déjà utilisée.'
      : authError.message
    return { error: msg }
  }

  const authUserId = authData.user.id

  // ── 4. Create profile ─────────────────────────────────────
  const { data: profileData, error: profileError } = await admin
    .from('profiles')
    .insert({
      user_id: authUserId,
      full_name: input.full_name.trim(),
      phone: input.phone?.trim() || null,
      role: 'student',
      driving_school_id: schoolId,
    })
    .select('id')
    .single()

  if (profileError) {
    await admin.auth.admin.deleteUser(authUserId)
    return { error: profileError.message }
  }

  // ── 5. Insert student record ──────────────────────────────
  const { error: studentError } = await admin.from('students').insert({
    driving_school_id: schoolId,
    profile_id: profileData.id,
    full_name: input.full_name.trim(),
    phone: input.phone?.trim() || null,
    email: input.email.trim(),
    license_category: input.license_category,
  })

  if (studentError) {
    await admin.from('profiles').delete().eq('id', profileData.id)
    await admin.auth.admin.deleteUser(authUserId)
    return { error: studentError.message }
  }

  revalidatePath('/dashboard/students')

  // Return the auto-generated password so the admin can hand it to the student.
  // If the admin set their own password, they already know it.
  return { createdPassword: isAutoGenerated ? password : undefined }
}

export async function updateTrainingStatus(
  studentId: string,
  status: TrainingStatus
): Promise<{ error?: string }> {
  const supabase = await createClient()

  const { data: { user } } = await supabase.auth.getUser()
  if (!user) return { error: 'Non authentifié.' }

  const { data: profile } = await supabase
    .from('profiles')
    .select('role')
    .eq('user_id', user.id)
    .single()

  if (!profile) return { error: 'Profil introuvable.' }

  const canUpdate = profile.role === 'school_admin' || profile.role === 'super_admin'
  if (!canUpdate) return { error: 'Accès refusé.' }

  const { data, error } = await supabase
    .from('students')
    .update({ training_status: status })
    .eq('id', studentId)
    .select('id')
    .single()

  if (error || !data) {
    return { error: "Mise à jour impossible. Vérifiez vos droits d'accès." }
  }

  revalidatePath('/dashboard/students')
  revalidatePath(`/dashboard/students/${studentId}`)
  return {}
}
